    private void kButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton2ActionPerformed
        // TODO add your handling code here:
        String id = txtid.getText();
        String firstname = txtfirstname.getText();
        String lastname = txtlastname.getText();
       
        
        String address = txtaddress.getText();
        
        DateFormat dt = new SimpleDateFormat("yyyy-mm-dd");
        String date = dt.format(txtdate.getDate());
        String gender;
        
        if(r1.isSelected()){
            gender="Male";
        }else{
            gender="Female";
        }
        String contact = txtcontact.getText();
        
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/train","root","");
            pt =con.prepareStatement("insert into addcustomer(id,firstname,lastname,age,gender,address,contact)value(?,?,?,?,?,?,?)");
            
            pt.setString(1,id);
            pt.setString(2,firstname);
            pt.setString(3,lastname);
            pt.setString(4,date);
            pt.setString(5,gender);
            pt.setString(6,address);
            pt.setString(7,contact);
          
            
            pt.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null, "record updated success");
            System.out.println("complete");
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(addcustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_kButton2ActionPerformed

